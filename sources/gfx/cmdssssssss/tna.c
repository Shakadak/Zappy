void	tna(t_env *gfx, char **param)
{
	int		i;

	i = 0;
	while (i < 256)
	{
		if (gfx->g->teams[i]->name == NULL)
		{
			gfx->g->teams[i]->name = ft_strdup(param[1]);
			break ;
		}
		i++;
	}
}
